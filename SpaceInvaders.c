void makeAMove(int position , int playerNum) 
{
	int i = 0 ;
	for( i = 5 ; i > 0 ; i--)
	{
		if( gameS[i][position] > 0 )
			continue;
		
		gameS[i][position] = playerNum ;
		Nokia5110_PrintBMP( 13 +(8 * (position-1)) ,6 + (8 * i) , Chips[playerNum].image, 0);
		Nokia5110_DisplayBuffer();
		break;
	}
	
}
void grideInit()
{
	int i = 0 , j = 0 ;
	for(i = 0 ; i < 7 ; i++){
		for(j = 0 ; j < 7 ; j++) {
			gameS[i][j] = 0 ; 
		}
	}
}
int checkTheWinner(int player)
{
	 int i, j;
 
    
    for(i=0; i<6; i++){
        for(i=0; i<6; i++)
        for(j=0; j<=7-4; j++){
            if(gameS[i][j]== player && gameS[i][j+1]== player && gameS[i][j+2]== player && gameS[i][j+3]== player)
                return player;
        }
 
    
    for(i=0; i<=6-4; i++)
        for(j=0; j<7; j++){
            if(gameS[i][j]== player && gameS[i+1][j]== player && gameS[i+2][j]== player && gameS[i+3][j]== player )
                return player;
        }
 
  
    for(i=6-1; i>=4-1; i--)
        for(j=0; j<=7-4; j++){
            if(gameS[i][j]== player && gameS[i-1][j+1]== player && gameS[i-2][j+2]== player && gameS[i-3][j+3]== player)
                return player;
        }
 
    
    for(i=0; i<=6-4; i++)
        for(j=0; j<=7-4; j++){
            if(gameS[i][j]== player && gameS[i+1][j+1]== player && gameS[i+2][j+2]== player && gameS[i+3][j+3]== player)
                return player;
        }
 
 
   
    for(i=0; i<7; i++)
        if(gameS[6-1][i]==0)
            return 0;
        }
 
    return 3; 
	
}
	
void PortF_Init(void){ volatile unsigned long delay;
  SYSCTL_RCGC2_R |= 0x00000020;     // 1) F clock
  delay = SYSCTL_RCGC2_R;           // delay   
  GPIO_PORTF_LOCK_R = 0x4C4F434B;   // 2) unlock PortF PF0  
  GPIO_PORTF_CR_R = 0x1F;           // allow changes to PF4-0       
  GPIO_PORTF_AMSEL_R = 0x00;        // 3) disable analog function
  GPIO_PORTF_PCTL_R = 0x00000000;   // 4) GPIO clear bit PCTL  
  GPIO_PORTF_DIR_R = 0x0E;          // 5) PF4,PF0 input, PF3,PF2,PF1 output   
  GPIO_PORTF_AFSEL_R = 0x00;        // 6) no alternate function
  GPIO_PORTF_PUR_R = 0x11;          // enable pullup resistors on PF4,PF0       
  GPIO_PORTF_DEN_R = 0x1F;          // 7) enable digital pins PF4-PF0 
}

